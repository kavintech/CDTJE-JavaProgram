
public class ELSEIFLADDER {

	public static void main(String[] args) {
		int rollNumber[]={200,300,444,322,121};
		
		int marks[]={30,50,67,75,99};
		for(int i=0;i<rollNumber.length;i++)
		{
			if(marks[i]>70)
				System.out.println(rollNumber[i]+"1st class");
		
		else if(marks[i]>50)
		System.out.println(rollNumber[i]+"2nd class");
		else if(marks[i]>35)
		System.out.println(rollNumber[i]+"3rd class");
		else
			System.out.println(rollNumber[i]+"fail");
}
}
}