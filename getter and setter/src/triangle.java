
public class triangle 
	 {
	 	private double base,height; 
	 	
	 	public void setBase(double a)
	 	{
	 		base=(a>0)?a:1;
	 	}
	 	
	 	public double getBase()
	 	{
	 		return base;
	 	}

	 	public double getHeight() 
	 	{
	 		return height;
	 	}

	 	public void setHeight(double height) 
	 	{
	 		this.height = (height>0)?height:1;
	 	}
	 	
	 	
	 }




